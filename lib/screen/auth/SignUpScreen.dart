  Future<void> _signUp() async {
    if (!_formKey.currentState!.validate()) {
      return;
    }

    setState(() {
      _isLoading = true;
    });

    try {
      final email = _emailController.text.trim();
      final password = _passwordController.text;
      final username = _usernameController.text.trim();
      final phone = _phoneController.text.trim();

      // 1. Sign up ‡∏î‡πâ‡∏ß‡∏¢ Supabase Auth ‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏™‡πà‡∏á username ‡πÅ‡∏•‡∏∞ phone ‡πÉ‡∏ô metadata
      final authResponse = await Supabase.instance.client.auth.signUp(
        email: email,
        password: password,
        data: {
          'username': username,
          'phone_number': phone,
        },
      );

      if (authResponse.user == null) {
        throw Exception('‡πÑ‡∏°‡πà‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏ö‡∏±‡∏ç‡∏ä‡∏µ‡πÑ‡∏î‡πâ');
      }

      final userId = authResponse.user!.id;
      print('‚úÖ Signup ‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à - User ID: $userId');

      // 2. ‡∏™‡∏£‡πâ‡∏≤‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• student ‡πÇ‡∏î‡∏¢‡∏ï‡∏£‡∏á (‡πÑ‡∏°‡πà‡∏£‡∏≠ trigger)
      final studentId = email.split('@')[0]; // 66050713
      final currentYear = DateTime.now().year + 543; // 2568
      final studentYear = int.parse(studentId.substring(0, 2)); // 66
      final year = currentYear - (2500 + studentYear);
      final actualYear = year < 1 ? 1 : (year > 5 ? 5 : year);

      print('üìù Creating student record: $studentId, Year: $actualYear');

      await Supabase.instance.client.from('students').insert({
        'id': userId,
        'student_id': studentId,
        'email': email,
        'username': username,
        'phone_number': phone,
        'faculty': '‡∏ß‡∏¥‡∏ó‡∏¢‡∏≤‡∏®‡∏≤‡∏™‡∏ï‡∏£‡πå',
        'year': actualYear,
        'university': '‡∏™‡∏ñ‡∏≤‡∏ö‡∏±‡∏ô‡πÄ‡∏ó‡∏Ñ‡πÇ‡∏ô‡πÇ‡∏•‡∏¢‡∏µ‡∏û‡∏£‡∏∞‡∏à‡∏≠‡∏°‡πÄ‡∏Å‡∏•‡πâ‡∏≤‡πÄ‡∏à‡πâ‡∏≤‡∏Ñ‡∏∏‡∏ì‡∏ó‡∏´‡∏≤‡∏£‡∏•‡∏≤‡∏î‡∏Å‡∏£‡∏∞‡∏ö‡∏±‡∏á',
        'profile_completed': true,
      });

      print('‚úÖ Student record created successfully');

      if (mounted) {
        // ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à
        NotificationHelper.showSuccess(
          context,
          '‡∏™‡∏°‡∏±‡∏Ñ‡∏£‡∏™‡∏°‡∏≤‡∏ä‡∏¥‡∏Å‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à! ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÄ‡∏Ç‡πâ‡∏≤‡∏™‡∏π‡πà‡∏£‡∏∞‡∏ö‡∏ö...',
        );

        // ‡∏£‡∏≠ 1 ‡∏ß‡∏¥‡∏ô‡∏≤‡∏ó‡∏µ‡πÅ‡∏•‡πâ‡∏ß navigate
        await Future.delayed(const Duration(seconds: 1));

        if (mounted) {
          // main.dart StreamBuilder ‡∏à‡∏∞‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£ navigation ‡πÉ‡∏´‡πâ‡πÄ‡∏≠‡∏á
          Navigator.pop(context);
        }
      }
    } on AuthException catch (e) {
      String errorMessage = '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î';
      
      print('‚ùå AuthException: ${e.message}');
      
      if (e.message.contains('already registered') || e.message.contains('already been registered')) {
        errorMessage = '‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡∏ô‡∏µ‡πâ‡∏ñ‡∏π‡∏Å‡πÉ‡∏ä‡πâ‡∏á‡∏≤‡∏ô‡πÅ‡∏•‡πâ‡∏ß';
      } else if (e.message.contains('Invalid email')) {
        errorMessage = '‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡∏≠‡∏µ‡πÄ‡∏°‡∏•‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á';
      } else if (e.message.contains('Password')) {
        errorMessage = '‡∏£‡∏´‡∏±‡∏™‡∏ú‡πà‡∏≤‡∏ô‡πÑ‡∏°‡πà‡∏õ‡∏•‡∏≠‡∏î‡∏†‡∏±‡∏¢‡∏û‡∏≠';
      } else {
        errorMessage = '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î: ${e.message}';
      }

      if (mounted) {
        NotificationHelper.showError(
          context,
          errorMessage,
        );
      }
    } catch (e) {
      print('‚ùå Error signing up: $e');
      if (mounted) {
        NotificationHelper.showError(
          context,
          '‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏•‡∏≠‡∏á‡πÉ‡∏´‡∏°‡πà‡∏≠‡∏µ‡∏Å‡∏Ñ‡∏£‡∏±‡πâ‡∏á',
        );
      }
    } finally {
      if (mounted) {
        setState(() {
          _isLoading = false;
        });
      }
    }
  }

